#Fundamental Data Types
#These are all integers
print(2 + 4)
print(2 - 4)
print(2 * 4)
print(2 / 4)

print(type(10.56))

print(2 ** 2) #2 ^2
print(2 // 2) #Does the division and returns a rounded down integer
print(5 % 4) #Basic modulo

# Math Functions
print('Printing the rounded version of 3.1')
print(round(3.1))
print('Printing the absolute power of -20')
print(abs(-20))
